//Pintar la tabla de multiplicar de un numero
public class EjercicioTablaMultiplicar {

	public static void main(String[] args) {
		
		int numero = 8;
		for ( int i=1 ;  i<=10  ; i++ ) {
			System.out.println(i + "x" + numero + "=" + i*numero );
		}

	}

}
